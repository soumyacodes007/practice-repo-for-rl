@app.post("/grader")
async def run_grader(episode_data: dict):
    """
    Return grader score after an episode is completed.
    Required for hackathon validation.
    
    Expected input:
    {
        "final_time_ms": 50.0,
        "target_time_ms": 100.0,
        "steps_taken": 3,
        "max_steps": 10
    }
    """
    try:
        final_ms = episode_data.get("final_time_ms", 0.0)
        target_ms = episode_data.get("target_time_ms", 100.0)
        steps_taken = episode_data.get("steps_taken", 0)
        max_steps = episode_data.get("max_steps", 10)
        
        # Compute final grade (0.0 - 1.0)
        grade = Grader.grade_final(final_ms, target_ms)
        
        # Compute terminal bonus if solved
        bonus = 0.0
        if final_ms <= target_ms:
            bonus = Grader.compute_terminal_bonus(final_ms, target_ms, steps_taken, max_steps)
        
        return {
            "score": grade,
            "bonus": bonus,
            "total_score": grade + (bonus / 10.0),  # Normalize bonus
            "solved": final_ms <= target_ms,
            "speedup": target_ms / final_ms if final_ms > 0 else 0.0
        }
    except Exception as e:
        logger.error(f"Grader error: {e}")
        raise HTTPException(status_code=400, detail=str(e))



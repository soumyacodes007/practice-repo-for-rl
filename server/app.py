"""
FastAPI server for LLM Text2SQL Failure Gym.

HTTP server with session management for hackathon validation.
"""

import logging
from typing import Dict
from fastapi import FastAPI, HTTPException
from pydantic import BaseModel, Field
from server.environment import LLMQueryOptimizerEnvironment
from server.fault_injector import TASKS
from server.grader import Grader
from models import QueryAction, QueryObservation

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s: %(message)s"
)

logger = logging.getLogger(__name__)

# Create FastAPI app
app = FastAPI(title="LLM Text2SQL Failure Gym")

# Global session store for HTTP-based state management
sessions: Dict[str, LLMQueryOptimizerEnvironment] = {}

# Request/Response models

class ResetRequest(BaseModel):
    task_id: int = Field(default=1, ge=1, le=3)
    session_id: str = Field(default="default")

class StepRequest(BaseModel):
    action: QueryAction
    session_id: str = Field(default="default")

# Core endpoints with session management

@app.post("/reset")
async def reset(request: ResetRequest):
    """
    Reset environment with session management.
    Required for HTTP-based state persistence.
    """
    session_id = request.session_id
    task_id = request.task_id
    
    logger.info(f"Reset called for session: {session_id}, task: {task_id}")
    
    # Create fresh environment for this session
    env = LLMQueryOptimizerEnvironment()
    obs = env.reset(task_id=task_id)
    
    # Store in session
    sessions[session_id] = env
    
    logger.info(f"Session {session_id} created, {len(sessions)} active sessions")
    
    return {
        "observation": obs.model_dump(),
        "reward": None,
        "done": False
    }

@app.post("/step")
async def step(request: StepRequest):
    """
    Execute step with session management.
    Required for HTTP-based state persistence.
    """
    session_id = request.session_id
    
    logger.info(f"Step called for session: {session_id}")
    
    # Retrieve existing environment
    if session_id not in sessions:
        logger.error(f"Session {session_id} not found. Active sessions: {list(sessions.keys())}")
        raise HTTPException(
            status_code=400,
            detail="Session not found. Call /reset first with the same session_id."
        )
    
    env = sessions[session_id]
    obs = env.step(request.action)
    
    # Extract reward and done from observation
    reward = obs.reward if hasattr(obs, 'reward') else None
    done = obs.done if hasattr(obs, 'done') else False
    
    # Clean up if done
    if done:
        logger.info(f"Episode done, cleaning up session {session_id}")
        env.close()
        del sessions[session_id]
    
    return {
        "observation": obs.model_dump(),
        "reward": reward,
        "done": done,
        "info": {}
    }

@app.get("/state")
async def get_state(session_id: str = "default"):
    """Get current environment state."""
    if session_id not in sessions:
        return {"error": "Session not found"}
    
    env = sessions[session_id]
    return env.state.model_dump()

